#바인딩(binding) : 컴퓨터 프로그래밍에서 각종 값들이 확정되어 더 이상 변경할 수 없는 구속(bind) 상태가 되는 것. 프로그램 내에서 변수, 배열, 라벨, 절차 등의 명칭.
#                 즉 식별자(identifier)가 그 대상인 메모리 주소, 데이터형 또는 실제값으로 배정되는 것이 이에 해당된다.
#정적 바인딩(static binding) : 원시 프로그램의 컴파일링 또는 링크 시에 확정되는 바인딩.
#동적 바인딩(dynamic binding) : 프로그램의 실행되는 과정에서 진행되는 바인딩.

